
package Clase1;

public class CiclosWhile {
    public static void main(String[] args) {
        
        var cont = 0; // inferencia de tipos
        
        while (cont < 5) {
            System.out.println("Conteo = " + cont);   //soutv + Tab
            cont++; // aumenta en 1 la variable
            
        }
        
    }
    
}
