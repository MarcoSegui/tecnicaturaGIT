//Leccion 1: Ciclo while

let cont = 0;

while (cont < 3){
    console.log(cont);
    cont ++;
}

console.log('Cierre del ciclo While');

// Leccion 2: Ciclo Do While

let cont1 = 0;

do {
    console.log(cont1);
    cont1++;
} while(cont1 < 3);

console.log('Cierre del ciclo Do While');

// Leccion 3: Ciclo For

for (let cont2 = 0; cont2 < 3; cont2++){
    console.log(cont2);
}

console.log('Cierre del ciclo For');


